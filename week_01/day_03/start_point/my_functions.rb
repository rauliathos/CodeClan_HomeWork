
#---------------add_length_of_arrays-----------------#

def add_array_lengths(arr1, arr2)
  return arr1.length + arr2.length
end

#----------------------sum_array----------------------#

def sum_array(number)
  return number.sum()
end

#----------------------find_item----------------------#

def find_item(array, item)
  return array.include?(item)
end

#----------------------get_first_key----------------------#

def get_first_key(hash)
  return hash.keys[0]
end
